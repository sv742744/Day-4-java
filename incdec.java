import java.util.*;
 public class incdec {
    public static void main (String args[]){
        int s=5;
        System.out.println(s++);// post increamnet//
        System.out.println(++s);//pre increament//
        System.out.println(--s);//pre decrreament//
        System.out.println(s--);//post decreament//
    }
 }